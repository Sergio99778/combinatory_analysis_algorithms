def dfs(graph, start, visited, path, length, longest):
    visited[start] = True
    path.append(start)

    for neighbor, connected in enumerate(graph[start]):
        if connected and not visited[neighbor]:
            dfs(graph, neighbor, visited, path, length + 1, longest)
        elif connected and neighbor == path[0] and length > len(longest):
            longest.clear()
            longest.extend(path)
            longest.append(neighbor)  # Completa el ciclo

    path.pop()
    visited[start] = False

def longest_cycle(graph):
    if not graph:
        return []

    longest = []
    for start in range(len(graph)):
        visited = [False] * len(graph)
        path = []
        dfs(graph, start, visited, path, 1, longest)
    return longest

